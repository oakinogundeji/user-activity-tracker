'use strict';
//=============================================================================
/**
 * dependencies
 */
//=============================================================================
const
    Vue = require('vue'),
    VueRouter = require('vue-router');
//=============================================================================
/**
 * config
 */
//=============================================================================
Vue.use(VueRouter);
//=============================================================================
/**
 * components
 */
//=============================================================================
const
    AppNav = require('./components/app_nav'),
    AppHome = require('./components/app_home'),
    AppAbout = require('./components/app_about'),
    AppServices = require('./components/app_services'),
    AppFooter = require('./components/app_footer');
//=============================================================================
/**
 * router
 */
//=============================================================================
const
    routes = [
        {
            path: '/',
            name: 'home',
            component: AppHome
        },
        {
            path: '/about',
            name: 'about',
            component: AppAbout
        },
        {
            path: '/services',
            name: 'services',
            component: AppServices
        }
    ],
    router = new VueRouter({routes});
//=============================================================================
/**
 * base VM
 */
//=============================================================================
const VM = new Vue({
    router,
    components: {
        'app-nav': AppNav,
        'app-footer': AppFooter
    }
});
//=============================================================================
/**
 * mount VM to View
 */
//=============================================================================
VM.$mount('#app');
//=============================================================================
